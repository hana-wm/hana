// layout_flags is a dedicated build-options module generated by build.zig
// after discoverModules runs. It contains one has_X bool per known layout
// name, reflecting which files are actually present on disk.
const layout_flags = @import("layout_flags");

const LayoutStub = struct {
    pub fn tileWithOffset(
        _: *const layouts.LayoutCtx,
        _: anytype,
        _: []const u32,
        _: u16, _: u16, _: u16,
    ) void {}
};

const master_layout    = if (layout_flags.has_master)    @import("master")    else LayoutStub;
const monocle_layout   = if (layout_flags.has_monocle)   @import("monocle")   else LayoutStub;
const grid_layout      = if (layout_flags.has_grid)      @import("grid")      else LayoutStub;
const fibonacci_layout = if (layout_flags.has_fibonacci) @import("fibonacci") else LayoutStub;
const fullscreen = @import("fullscreen");

const MAX_MASTER_WIDTH: f32 = 0.95;
// Stack-local buffer size for per-retile workspace window list.
const MAX_WS_WINDOWS: usize = 128;

/// Bitmask with only the bit for `ws_idx` set.
inline fn wsBit(ws_idx: anytype) u64 { return @as(u64, 1) << @intCast(ws_idx); }

/// Sentinel rect value meaning "no valid geometry cached".
const ZERO_RECT: utils.Rect = .{ .x = 0, .y = 0, .width = 0, .height = 0 };

pub const Layout = enum { master, monocle, grid, fibonacci };

// Layouts present on disk at build time. toggleLayout/toggleLayoutReverse walk
// this list so missing layouts are never visited during cycling.
// A compile error fires if every layout file has been removed.
const LAYOUT_CYCLE: []const Layout = blk: {
    var list: []const Layout = &.{};
    if (layout_flags.has_master)    list = list ++ &[_]Layout{.master};
    if (layout_flags.has_monocle)   list = list ++ &[_]Layout{.monocle};
    if (layout_flags.has_grid)      list = list ++ &[_]Layout{.grid};
    if (layout_flags.has_fibonacci) list = list ++ &[_]Layout{.fibonacci};
    if (list.len == 0) @compileError("No tiling layouts found. Add at least one .zig file to src/tiling/layouts/.");
    break :blk list;
};

/// Returns the first layout available at build time (fallback default).
pub inline fn defaultLayout() Layout { return LAYOUT_CYCLE[0]; }

/// True when the module file for `layout` was present at build time.
pub inline fn isLayoutAvailable(layout: Layout) bool {
    return switch (layout) {
        .master    => layout_flags.has_master,
        .monocle   => layout_flags.has_monocle,
        .grid      => layout_flags.has_grid,
        .fibonacci => layout_flags.has_fibonacci,
    };
}

// Walk the LAYOUT_CYCLE slice to find `current`, then step forward or backward.
// The list is at most 4 elements long; linear scan is intentional and fine.
// If `current` names a layout whose file was removed (not in LAYOUT_CYCLE),
// we jump to the first available entry.
inline fn stepCycle(current: Layout, comptime forward: bool) Layout {
    for (LAYOUT_CYCLE, 0..) |l, i| {
        if (l == current) return LAYOUT_CYCLE[
            if (forward) (i + 1) % LAYOUT_CYCLE.len
            else         (LAYOUT_CYCLE.len + i - 1) % LAYOUT_CYCLE.len
        ];
    }
    return LAYOUT_CYCLE[0];
}

// Variation enums are defined in defs.zig to allow config.zig to parse them
// without a circular import. Re-exported here for convenience.
pub const MasterVariation  = defs.MasterVariation;
pub const MonocleVariation = defs.MonocleVariation;
pub const GridVariation    = defs.GridVariation;

pub const LayoutVariations = struct {
    master:  MasterVariation  = .lifo,
    monocle: MonocleVariation = .gapless,
    grid:    GridVariation    = .rigid,
};

// State

pub const State = struct {
    allocator:        std.mem.Allocator,
    enabled:          bool,
    layout:           Layout,
    layout_variations: LayoutVariations,
    /// 3-character indicator shown in the bar for the fibonacci layout.
    fibonacci_indicator: [3]u8,
    master_side:      defs.MasterSide,
    master_width:     f32,
    master_count:     u8,
    gap_width:        u16,
    border_width:     u16,
    border_focused:   u32,
    border_unfocused: u32,
    windows:          Tracking,
    dirty:            bool,

    /// Per-workspace geometry validity bitmask (64 bits → up to 64 workspaces).
    ///
    /// Bit N is set when workspace N's geometry has been pre-computed and the
    /// cache holds correct on-screen positions for all its windows.
    /// Widened from u32 to u64 to support up to 64 workspaces at zero extra
    /// runtime cost on 64-bit targets.
    ///
    /// Cleared by: addWindow, removeWindow, adjustMasterWidth,
    ///             syncLayoutFromWorkspace.
    /// Set by the retile call that immediately follows each of those.
    ws_geom_valid:    u64,

    /// Screen area (x, y, width, height) used in the most recent retile call.
    /// restoreWorkspaceGeom rejects the cache if this differs from the current
    /// screen area (e.g. after a bar height or position change).
    last_retile_screen: utils.Rect,

    /// Combined per-window cache: last geometry AND last border color in a
    /// single hash table, halving the number of hash lookups vs the previous
    /// pair of separate geom_cache / border_cache maps.
    ///
    /// Populated by configureSafe (rect field) and sendBorderColor (border
    /// field). Both use getOrPut so one probe handles dedup + insert.
    cache: layouts.CacheMap,

    pub inline fn margins(self: *const State) utils.Margins {
        return .{ .gap = self.gap_width, .border = self.border_width };
    }

    pub inline fn borderColor(self: *const State, win: u32) u32 {
        if (fullscreen.isFullscreen(win)) return 0;
        return if (focus.getFocused() == win) self.border_focused else self.border_unfocused;
    }

    pub fn deinit(self: *State) void {
        self.windows.deinit();
        self.cache.deinit(self.allocator);
    }
};

// Module singleton

// Module singleton — guaranteed live after init(), never null during normal operation.
// g_initialized guards debug assertions; production builds pay zero cost.
var g_state:       State = undefined;
var g_initialized: bool  = false;

/// Returns a pointer to the live state.
/// Asserts in Debug builds that init() has been called.
pub inline fn getState() *State {
    std.debug.assert(g_initialized);
    return &g_state;
}

/// Safe pre-init query for code that may run before the event loop starts.
/// Returns null only during the narrow startup window before init() is called.
pub inline fn getStateOpt() ?*State {
    return if (g_initialized) &g_state else null;
}

// Config

fn computeMasterWidth(wm: *WM) f32 {
    const raw = dpi.scaleMasterWidth(wm.config.tiling.master_width);
    if (raw < 0) {
        const ratio = -raw / @as(f32, @floatFromInt(wm.screen.width_in_pixels));
        return @min(MAX_MASTER_WIDTH, @max(constants.MIN_MASTER_WIDTH, ratio));
    }
    return raw;
}

fn buildState(wm: *WM) State {
    const screen_height = wm.screen.height_in_pixels;
    return .{
        .allocator        = wm.allocator,
        .enabled          = wm.config.tiling.enabled,
        .layout           = blk: {
            const requested = std.meta.stringToEnum(Layout, wm.config.tiling.layout)
                orelse LAYOUT_CYCLE[0];
            break :blk if (isLayoutAvailable(requested)) requested else LAYOUT_CYCLE[0];
        },
        .layout_variations = .{
            .master  = wm.config.tiling.master_variation,
            .monocle = wm.config.tiling.monocle_variation,
            .grid    = wm.config.tiling.grid_variation,
        },
        .fibonacci_indicator = wm.config.tiling.fibonacci_indicator,
        .master_side      = wm.config.tiling.master_side,
        .master_width     = computeMasterWidth(wm),
        .master_count     = wm.config.tiling.master_count,
        .gap_width             = dpi.scaleGaps(wm.config.tiling.gap_width, wm.dpi_info.scale_factor, screen_height),
        .border_width     = dpi.scaleBorderWidth(wm.config.tiling.border_width, wm.dpi_info.scale_factor, screen_height),
        .border_focused   = wm.config.tiling.border_focused,
        .border_unfocused = wm.config.tiling.border_unfocused,
        .windows          = Tracking.init(wm.allocator),
        .dirty            = false,
        .ws_geom_valid    = 0,
        .last_retile_screen = ZERO_RECT,
        .cache            = .{},
    };
}

pub fn init(wm: *WM) !void {
    g_state       = buildState(wm);
    g_initialized = true;
}

pub fn deinit() void {
    if (!g_initialized) return;
    g_state.deinit();
    g_initialized = false;
}

pub fn reloadConfig(wm: *WM) void {
    const s = getState();

    // Preserve runtime tracking state.
    const saved_windows = s.windows;

    // Clear the combined cache in-place, retaining the allocated capacity so
    // the next retile does not pay for a fresh heap allocation. The old data
    // is intentionally discarded: config changes (gaps, border width, colors)
    // make every cached rect and border color stale.
    var saved_cache = s.cache;
    saved_cache.clearRetainingCapacity();
    // Disown before buildState overwrites g_state; s is a dangling pointer
    // from this point forward and must not be used.
    s.cache      = .{};

    g_state = buildState(wm);
    const ns = &g_state;
    ns.windows    = saved_windows;
    ns.cache      = saved_cache;    // reuse the allocation

    // On config reload, reset all workspace layouts to the new default.
    if (workspaces.getState()) |ws_state| {
        for (ws_state.workspaces) |*ws| ws.layout = ns.layout;
    }

    if (ns.enabled) {
        // Wrap border-width push and retile in a single server grab so picom
        // never composites an intermediate frame where some windows have the
        // new border width but the layout has not yet been recalculated.
        _ = xcb.xcb_grab_server(wm.conn);
        for (ns.windows.items()) |win| {
            _ = xcb.xcb_configure_window(wm.conn, win,
                xcb.XCB_CONFIG_WINDOW_BORDER_WIDTH, &[_]u32{ns.border_width});
        }
        retileCurrentWorkspace(wm);
        bar.redrawImmediate(wm); // inside grab — must complete before ungrab
        _ = xcb.xcb_ungrab_server(wm.conn);
        _ = xcb.xcb_flush(wm.conn);
    }
}

/// Cache WM_NORMAL_HINTS minimum size constraints for `win`.
/// Called from window.zig at MapRequest time.
pub fn cacheSizeHints(allocator: std.mem.Allocator, win: u32, hints: layouts.SizeHints) void {
    if (hints.min_width == 0 and hints.min_height == 0) return;
    layouts.cacheSizeHints(allocator, win, hints);
}

/// Evict the size-hint entry for `win`. Called from window.zig at unmanage time.
pub fn evictSizeHints(win: u32) void {
    layouts.evictSizeHints(win);
}

// Window management 

pub fn addWindow(wm: *WM, window_id: u32) void {
    std.debug.assert(window_id != 0);
    const s = getState();
    if (!s.enabled) return;

    const result = if (s.layout == .master and s.layout_variations.master == .fifo)
        s.windows.addFront(window_id)
    else
        s.windows.add(window_id);
    result catch |err| { debug.logError(err, window_id); return; };
    s.dirty = true;
    s.ws_geom_valid = 0;

    const border_color = s.borderColor(window_id);
    _ = xcb.xcb_change_window_attributes(wm.conn, window_id,
        xcb.XCB_CW_BORDER_PIXEL, &[_]u32{border_color});
    _ = xcb.xcb_configure_window(wm.conn, window_id,
        xcb.XCB_CONFIG_WINDOW_BORDER_WIDTH, &[_]u32{s.border_width});

    // Pre-populate the cache so the immediately-following retile does not
    // re-send the border pixel.
    const gop = s.cache.getOrPut(s.allocator, window_id) catch return;
    gop.value_ptr.border = border_color;
    if (!gop.found_existing) gop.value_ptr.rect = ZERO_RECT;
}

pub fn removeWindow(window_id: u32) void {
    const s = getState();
    if (s.windows.remove(window_id)) {
        s.dirty = true;
        s.ws_geom_valid = 0;
        _ = s.cache.remove(window_id);
    }
}

/// Toggle a window between tiled and floating.
///
/// Tiled → floating: removes the window from the tiling pool so it sits
/// freely at its current position.
///
/// Floating → tiled: hands the window back to the tiling pool (respecting
/// the current layout's LIFO/FIFO insertion rule) and immediately retiles
/// the workspace so the new window gets a proper slot.
pub fn toggleWindowFloat(wm: *WM, window_id: u32) void {
    const s = getState();
    if (!s.enabled) return;

    if (s.windows.contains(window_id)) {
        removeWindow(window_id);
        debug.info("[FLOAT] 0x{x} → floating", .{window_id});
    } else {
        addWindow(wm, window_id);
        debug.info("[FLOAT] 0x{x} → tiled", .{window_id});
    }
    retileCurrentWorkspace(wm);
    _ = xcb.xcb_flush(wm.conn);
}

/// Save geometry for any window (tiled or floating) into the shared cache.
/// Called by the workspace switcher before pushing windows off-screen so that
/// floating windows can be restored to their exact position on return.
pub fn saveWindowGeom(window_id: u32, rect: utils.Rect) void {
    const s = getState();
    const gop = s.cache.getOrPut(s.allocator, window_id) catch return;
    gop.value_ptr.rect = rect;
    if (!gop.found_existing) gop.value_ptr.border = 0;
}

/// Return the cached geometry for any window (tiled or floating).
/// Returns null when no entry exists or the entry was invalidated (zeroed rect).
pub inline fn getWindowGeom(window_id: u32) ?utils.Rect {
    const s = getStateOpt() orelse return null;
    const wd = s.cache.get(window_id) orelse return null;
    if (wd.rect.width == 0 and wd.rect.height == 0) return null;
    return wd.rect;
}

// Evict a window's rect from the cache without removing it from tiling.
// Call whenever a window's position is changed outside the normal retile path
// (e.g. pushed offscreen during fullscreen) so the next retile does not find
// a stale cache hit and skip configure_window.
// The border entry is preserved; only the rect is zeroed.
pub fn invalidateGeomCache(window_id: u32) void {
    const s = getState();
    if (s.cache.getPtr(window_id)) |wd| {
        wd.rect = ZERO_RECT;
    }
}

/// Clear the workspace-valid bit for `ws_idx` so the next restoreWorkspaceGeom
/// for that workspace triggers a full retile. Used when a window's tag changes
/// for an inactive workspace without touching the current one.
pub inline fn invalidateWsGeomBit(ws_idx: u8) void {
    const s = getState();
    if (ws_idx < 64) s.ws_geom_valid &= ~wsBit(ws_idx);
}

/// Mark tiling dirty without immediately retriling.
pub inline fn dirty() void {
    const s = getState();
    s.dirty = true;
}

// Restore windows on the current workspace to their cached tiled positions,
// bypassing the layout algorithm.
//
// Returns true  if cache is valid and positions have been replayed.
// Returns false if cache is stale; caller must fall back to retileCurrentWorkspace.
pub fn restoreWorkspaceGeom(wm: *WM) bool {
    const s = getStateOpt() orelse return false;

    var ws_buf: [MAX_WS_WINDOWS]u32 = undefined;
    const ws_count = filterWorkspaceWindows(s, &ws_buf, null);
    const ws_windows = ws_buf[0..ws_count];
    if (ws_windows.len == 0) return true;

    const current_ws = workspaces.getCurrentWorkspace() orelse return false;
    if (current_ws >= 64) return false;
    if (s.ws_geom_valid & wsBit(current_ws) == 0) return false;

    const current_screen = calculateScreenArea(wm);
    if (!layouts.rectsEqual(current_screen, s.last_retile_screen)) return false;

    // Verify every window is cached before emitting any XCB calls.
    var rects: [MAX_WS_WINDOWS]utils.Rect = undefined;
    for (ws_windows, 0..) |win, i| {
        const wd = s.cache.get(win) orelse return false;
        // A zeroed rect (from invalidateGeomCache) means the entry is stale.
        if (wd.rect.width == 0 and wd.rect.height == 0) return false;
        rects[i] = wd.rect;
    }

    for (ws_windows, rects[0..ws_windows.len]) |win, rect| {
        utils.configureWindow(wm.conn, win, rect);
    }
    updateBorders(s, wm, ws_windows);
    return true;
}

pub inline fn isWindowTiled(window_id: u32) bool {
    const s = getStateOpt() orelse return false;
    return s.windows.contains(window_id);
}

// Layout dispatch 

/// Resolves the active layout for a workspace, respecting the global_layout flag.
fn resolveLayout(s: *State, ws_state: ?*workspaces.State, ws_idx: u8, global: bool) Layout {
    if (global) return s.layout;
    const wss = ws_state orelse return s.layout;
    return if (ws_idx < wss.workspaces.len) wss.workspaces[ws_idx].layout else s.layout;
}

fn dispatchLayout(layout: Layout, ctx: *const layouts.LayoutCtx, s: *State, wins: []const u32, screen: utils.Rect) void {
    const w = screen.width;
    const h = screen.height;
    const y: u16 = @intCast(screen.y);
    switch (layout) {
        .master    => master_layout.tileWithOffset(ctx, s, wins, w, h, y),
        .monocle   => monocle_layout.tileWithOffset(ctx, s, wins, w, h, y),
        .grid      => grid_layout.tileWithOffset(ctx, s, wins, w, h, y),
        .fibonacci => fibonacci_layout.tileWithOffset(ctx, s, wins, w, h, y),
    }
}

// Screen area 

inline fn calculateScreenArea(wm: *WM) utils.Rect {
    const bar_height: u16 = if (bar.isVisible()) bar.getBarHeight() else 0;
    const bar_at_bottom   = wm.config.bar.vertical_position == .bottom;
    return .{
        .x      = 0,
        .y      = if (bar_at_bottom) 0 else @intCast(bar_height),
        .width  = wm.screen.width_in_pixels,
        .height = wm.screen.height_in_pixels -| bar_height,
    };
}

// Retiling 

// Retile every tiled window on every workspace.
pub fn retileAllWorkspaces(wm: *WM) void {
    const s = getState();
    if (!s.enabled) return;

    const screen     = calculateScreenArea(wm);
    const ws_count   = workspaces.getWorkspaceCount();
    const current_ws = workspaces.getCurrentWorkspace() orelse return;

    const ws_state_opt = if (!wm.config.tiling.global_layout) workspaces.getState() else null;

    const ctx = layouts.LayoutCtx{
        .conn       = wm.conn,
        .cache      = &s.cache,
        .allocator  = s.allocator,
    };
    const MAX_WS: usize = 64; // matches the u64 ws_geom_valid bitmask width
    const effective_ws = @min(ws_count, MAX_WS);
    var bufs: [MAX_WS][MAX_WS_WINDOWS]u32 = undefined;
    var lens: [MAX_WS]usize               = @splat(0);

    for (s.windows.items()) |win| {
        const ws_idx = workspaces.getWorkspaceForWindow(win) orelse continue;
        if (ws_idx >= effective_ws) continue;
        if (lens[ws_idx] < MAX_WS_WINDOWS) {
            bufs[ws_idx][lens[ws_idx]] = win;
            lens[ws_idx] += 1;
        }
    }

    var ws_idx: u8 = 0;
    while (ws_idx < effective_ws) : (ws_idx += 1) {
        if (ws_idx == current_ws) continue;
        if (fullscreen.getForWorkspace(ws_idx)) |_| continue;

        const ws_windows = bufs[ws_idx][0..lens[ws_idx]];
        if (ws_windows.len == 0) continue;

        dispatchLayout(resolveLayout(s, ws_state_opt, ws_idx, wm.config.tiling.global_layout), &ctx, s, ws_windows, screen);
        updateBorders(s, wm, ws_windows);
        markWsGeomValid(s, ws_idx);
    }

    s.last_retile_screen = screen;
}

pub fn retileIfDirty(wm: *WM) void {
    const s = getState();
    if (!s.enabled or !s.dirty) return;
    retileCurrentWorkspace(wm);
}

pub fn retileCurrentWorkspace(wm: *WM) void {
    const s = getState();
    if (!s.enabled) return;
    retile(wm, calculateScreenArea(wm), null);
    s.dirty = false;
}

// Retile a specific inactive workspace so its geometry cache is correct
// before the user switches to it.
//
// Previously this mutated ws_state.current as a side channel so the internal
// retile helper would filter the right window set. That was fragile: any
// re-entrant event handler reading ws_state.current during the window would
// see a wrong value. Now we pass the target workspace index directly to retile
// via the `for_ws` parameter — no global state mutation required.
//
// MUST be called inside a server grab: retile transiently moves windows to
// their on-screen positions.
pub fn retileInactiveWorkspace(wm: *WM, ws_idx: u8) void {
    const s = getState();
    if (!s.enabled) return;

    const ws_state = workspaces.getState() orelse return;

    if (ws_idx == ws_state.current) {
        retileCurrentWorkspace(wm);
        return;
    }

    // Pass ws_idx directly; retile filters windows and marks ws_geom_valid
    // for that workspace without touching ws_state.current.
    retile(wm, calculateScreenArea(wm), ws_idx);

    // Push windows back offscreen while their workspace is inactive.
    // Do NOT invalidate the cache: restoreWorkspaceGeom will find the
    // ws_geom_valid bit set, a complete cache, and a matching screen rect,
    // and replay positions in one batch.
    for (ws_state.workspaces[ws_idx].windows.items()) |win| {
        _ = xcb.xcb_configure_window(wm.conn, win,
            xcb.XCB_CONFIG_WINDOW_X,
            &[_]u32{@bitCast(@as(i32, constants.OFFSCREEN_X_POSITION))});
    }
}

// Core retile implementation.
// `for_ws`: when non-null, process that specific workspace instead of the
//           current one.  This replaces the previous ws_state.current mutation
//           pattern used by retileInactiveWorkspace.
fn retile(wm: *WM, screen: utils.Rect, for_ws: ?u8) void {
    const s = getState();

    const target_ws: u8 = for_ws orelse
        @intCast(workspaces.getCurrentWorkspace() orelse return);

    if (fullscreen.getForWorkspace(target_ws)) |_| return;

    var ws_buf: [MAX_WS_WINDOWS]u32 = undefined;
    const ws_count = filterWorkspaceWindows(s, &ws_buf, for_ws);
    const ws_windows = ws_buf[0..ws_count];
    if (ws_windows.len == 0) return;

    // Build the layout context once; it carries the connection and the
    // cache pointer so configureSafe never touches module-level globals.
    const ctx = layouts.LayoutCtx{
        .conn       = wm.conn,
        .cache      = &s.cache,
        .allocator  = s.allocator,
    };
    dispatchLayout(resolveLayout(s, workspaces.getState(), target_ws, wm.config.tiling.global_layout), &ctx, s, ws_windows, screen);

    s.last_retile_screen = screen;
    updateBorders(s, wm, ws_windows);
    markWsGeomValid(s, target_ws);
}

// Border management 

// Send border pixel only if color changed since last send.
// Uses the merged cache (WindowData.border) — one getOrPut covers dedup + insert.
fn sendBorderColor(s: *State, conn: *xcb.xcb_connection_t, win: u32, color: u32) void {
    const gop = s.cache.getOrPut(s.allocator, win) catch {
        _ = xcb.xcb_change_window_attributes(conn, win, xcb.XCB_CW_BORDER_PIXEL, &[_]u32{color});
        return;
    };
    if (gop.found_existing and gop.value_ptr.border == color) return;
    gop.value_ptr.border = color;
    if (!gop.found_existing) gop.value_ptr.rect = ZERO_RECT;
    _ = xcb.xcb_change_window_attributes(conn, win, xcb.XCB_CW_BORDER_PIXEL, &[_]u32{color});
}

inline fn updateBorders(s: *State, wm: *WM, ws_windows: []const u32) void {
    for (ws_windows) |win| sendBorderColor(s, wm.conn, win, s.borderColor(win));
}

pub fn updateWindowFocus(wm: *WM, old_focused: ?u32, new_focused: ?u32) void {
    const s = getState();
    for ([2]?u32{ old_focused, new_focused }) |opt| {
        const win = opt orelse continue;
        if (!s.windows.contains(win)) continue;
        sendBorderColor(s, wm.conn, win, s.borderColor(win));
    }
}

// Window reordering 

// Move the window at from_idx to to_idx in tiling order.
// Buffer is capped at MAX_WS_WINDOWS to be consistent with all other
// per-workspace window buffers in this module (previously hard-coded at 256).
fn moveWindowToIndex(s: *State, from_idx: usize, to_idx: usize) void {
    if (from_idx == to_idx) return;
    const current = s.windows.items();

    var temp: [MAX_WS_WINDOWS]u32 = undefined;
    if (current.len > temp.len) {
        debug.warn("moveWindowToIndex: too many windows ({})", .{current.len});
        return;
    }

    const win = current[from_idx];
    var j: usize = 0;
    for (current, 0..) |w, i| {
        if (i == from_idx) continue;
        if (j == to_idx) { temp[j] = win; j += 1; }
        temp[j] = w;
        j += 1;
    }
    if (to_idx >= j) { temp[j] = win; j += 1; }
    s.windows.reorder(temp[0..j]);
}

pub fn swapWithMaster(wm: *WM) void {
    const s = getState();
    const focused = focus.getFocused() orelse return;
    if (!s.windows.contains(focused) or !workspaces.isOnCurrentWorkspace(focused)) return;

    const all = s.windows.items();
    if (all.len < 2) return;

    var focused_pos: ?usize = null;
    var master_pos:  ?usize = null;
    for (all, 0..) |win, i| {
        if (win == focused) focused_pos = i;
        if (master_pos == null and workspaces.isOnCurrentWorkspace(win)) master_pos = i;
        if (focused_pos != null and master_pos != null) break;
    }
    const fp = focused_pos orelse return;
    const mp = master_pos  orelse return;

    if (fp == mp) {
        for (all[mp + 1..], mp + 1..) |win, i| {
            if (workspaces.isOnCurrentWorkspace(win)) {
                moveWindowToIndex(s, i, mp);
                break;
            }
        }
    } else {
        moveWindowToIndex(s, fp, mp);
    }
    retileCurrentWorkspace(wm);
}

// Layout and master controls 

pub fn toggleTiling(wm: *WM) void {
    const s = getState();
    s.enabled = !s.enabled;
    if (s.enabled) retileCurrentWorkspace(wm);
    debug.info("Tiling {s}", .{if (s.enabled) "enabled" else "disabled"});
}

pub fn syncLayoutFromWorkspace(ws: *const workspaces.Workspace) void {
    const s = getState();
    const layout = ws.layout;
    const needs_retile = s.layout != layout or ws.variation != null;
    s.layout = layout;
    // Apply the workspace-pinned variation override when present; a null
    // variation means "use whatever the global default currently is", so we
    // leave layout_variations untouched in that case.
    if (ws.variation) |v| {
        switch (v) {
            .master    => |mv| s.layout_variations.master  = mv,
            .monocle   => |mv| s.layout_variations.monocle = mv,
            .grid      => |gv| s.layout_variations.grid    = gv,
            .fibonacci => {},
        }
    }
    if (needs_retile) {
        s.dirty = true;
        s.ws_geom_valid = 0;
    }
}

fn applyLayout(wm: *WM, s: *State, layout: Layout) void {
    s.layout = layout;
    if (!wm.config.tiling.global_layout)
        if (workspaces.getCurrentWorkspaceObject()) |ws| { ws.layout = layout; };
    retileCurrentWorkspace(wm);
    debug.info("Layout: {s}", .{@tagName(layout)});
}

pub fn toggleLayout(wm: *WM) void {
    const s = getState();
    applyLayout(wm, s, stepCycle(s.layout, true));
}

pub fn toggleLayoutReverse(wm: *WM) void {
    const s = getState();
    applyLayout(wm, s, stepCycle(s.layout, false));
}

pub fn adjustMasterCount(wm: *WM, delta: i8) void {
    const s = getState();
    const new: i16 = @as(i16, s.master_count) + delta;
    if (new < 0) return;
    const clamped: u8 = @intCast(@min(new, 10));
    if (clamped == s.master_count) return;
    s.master_count = clamped;
    retileCurrentWorkspace(wm);
}

pub inline fn increaseMasterCount(wm: *WM) void { adjustMasterCount(wm,  1); }
pub inline fn decreaseMasterCount(wm: *WM) void { adjustMasterCount(wm, -1); }

pub fn adjustMasterWidth(wm: *WM, delta: f32) void {
    const s = getState();
    s.master_width = @max(constants.MIN_MASTER_WIDTH, @min(MAX_MASTER_WIDTH, s.master_width + delta));
    s.dirty = true;
    s.ws_geom_valid = 0;
    retileCurrentWorkspace(wm);
}

pub inline fn increaseMasterWidth(wm: *WM) void { adjustMasterWidth(wm,  0.025); }
pub inline fn decreaseMasterWidth(wm: *WM) void { adjustMasterWidth(wm, -0.025); }

pub fn cycleLayoutVariation(wm: *WM) void {
    const s = getState();
    switch (s.layout) {
        .master => {
            s.layout_variations.master = switch (s.layout_variations.master) {
                .lifo => .fifo,
                .fifo => .lifo,
            };
            debug.info("Master variation: {s}", .{@tagName(s.layout_variations.master)});
        },
        .monocle => {
            s.layout_variations.monocle = switch (s.layout_variations.monocle) {
                .gapless => .gaps,
                .gaps    => .gapless,
            };
            debug.info("Monocle variation: {s}", .{@tagName(s.layout_variations.monocle)});
        },
        .grid => {
            s.layout_variations.grid = switch (s.layout_variations.grid) {
                .rigid   => .relaxed,
                .relaxed => .rigid,
            };
            debug.info("Grid variation: {s}", .{@tagName(s.layout_variations.grid)});
        },
        .fibonacci => {
            debug.info("Fibonacci has no variations", .{});
            return;
        },
    }
    retileCurrentWorkspace(wm);
    bar.markDirty();
}


// Internal helpers 

// Set the per-workspace geometry-valid bit. ws_idx must be < 64.
inline fn markWsGeomValid(s: *State, ws_idx: anytype) void {
    if (ws_idx < 64) s.ws_geom_valid |= wsBit(ws_idx);
}

// Collect windows belonging to the target workspace into buf.
// `for_ws`: when non-null, filter by that workspace index;
//           when null, filter by the current workspace.
fn filterWorkspaceWindows(s: *State, buf: []u32, for_ws: ?u8) usize {
    var n: usize = 0;
    for (s.windows.items()) |win| {
        if (n >= buf.len) break;
        const on_ws = if (for_ws) |idx|
            workspaces.isWindowOnWorkspace(win, idx)
        else
            workspaces.isOnCurrentWorkspace(win);
        if (on_ws) { buf[n] = win; n += 1; }
    }
    return n;
}
